#[cfg(test)]
mod tests {
    use crate::domain::negotiation::NegotiationSessionId;

    use super::*;

    fn test_envelope(ranges: Vec<ConcessionRange>) -> ConcessionEnvelope {
        ConcessionEnvelope {
            session_id: NegotiationSessionId("test-session".to_string()),
            ranges,
            blocking_reasons: Vec::new(),
        }
    }

    fn discount_range(current: f64) -> ConcessionRange {
        ConcessionRange {
            dimension: "discount_pct".to_string(),
            floor: 0.0,
            ceiling: 40.0,
            current,
        }
    }

    #[test]
    fn step_down_generates_ranked_alternatives() {
        let planner = CounterofferPlanner;
        let envelope = test_envelope(vec![discount_range(15.0)]);
        let config = CounterofferConfig {
            strategy: CounterofferStrategy::StepDown,
            max_alternatives: 3,
            ..Default::default()
        };

        let plan = planner.plan(&envelope, &config);

        assert_eq!(plan.alternatives.len(), 3);
        // Descending from ceiling
        assert!(plan.alternatives[0].discount_pct > plan.alternatives[1].discount_pct);
        assert!(plan.alternatives[1].discount_pct > plan.alternatives[2].discount_pct);
        // Ranks are 1, 2, 3
        assert_eq!(plan.alternatives[0].rank, 1);
        assert_eq!(plan.alternatives[1].rank, 2);
        assert_eq!(plan.alternatives[2].rank, 3);
    }

    #[test]
    fn split_difference_converges_toward_floor() {
        let planner = CounterofferPlanner;
        let envelope = test_envelope(vec![discount_range(30.0)]);
        let config = CounterofferConfig {
            strategy: CounterofferStrategy::SplitDifference,
            max_alternatives: 3,
            ..Default::default()
        };

        let plan = planner.plan(&envelope, &config);

        assert_eq!(plan.alternatives.len(), 3);
        // First offer is at current value
        assert_eq!(plan.alternatives[0].discount_pct, 30.0);
        // Second is midpoint between 30 and 0 = 15
        assert!((plan.alternatives[1].discount_pct - 15.0).abs() < 0.01);
        // Third is midpoint between 15 and 0 = 7.5
        assert!((plan.alternatives[2].discount_pct - 7.5).abs() < 0.01);
    }

    #[test]
    fn anchor_high_starts_near_ceiling() {
        let planner = CounterofferPlanner;
        let envelope = test_envelope(vec![discount_range(10.0)]);
        let config = CounterofferConfig {
            strategy: CounterofferStrategy::AnchorHigh,
            max_alternatives: 3,
            ..Default::default()
        };

        let plan = planner.plan(&envelope, &config);

        assert_eq!(plan.alternatives.len(), 3);
        // First offer should be close to ceiling (90% of 40 = 36)
        assert!(plan.alternatives[0].discount_pct > 30.0);
        // Descending
        assert!(plan.alternatives[0].discount_pct > plan.alternatives[1].discount_pct);
    }

    #[test]
    fn missing_tie_break_dimension_produces_empty_plan() {
        let planner = CounterofferPlanner;
        let envelope = test_envelope(vec![ConcessionRange {
            dimension: "margin_pct".to_string(),
            floor: 15.0,
            ceiling: 80.0,
            current: 40.0,
        }]);
        let config = CounterofferConfig {
            tie_break_field: "discount_pct".to_string(), // not in envelope
            ..Default::default()
        };

        let plan = planner.plan(&envelope, &config);

        assert!(plan.alternatives.is_empty());
    }

    #[test]
    fn deterministic_identical_inputs_identical_outputs() {
        let planner = CounterofferPlanner;
        let envelope = test_envelope(vec![discount_range(20.0)]);
        let config = CounterofferConfig::default();

        let plan1 = planner.plan(&envelope, &config);
        let plan2 = planner.plan(&envelope, &config);

        assert_eq!(plan1.alternatives.len(), plan2.alternatives.len());
        for (a, b) in plan1.alternatives.iter().zip(plan2.alternatives.iter()) {
            assert_eq!(a.offer_id, b.offer_id);
            assert_eq!(a.rank, b.rank);
            assert_eq!(a.discount_pct, b.discount_pct);
            assert_eq!(a.rationale, b.rationale);
        }
    }

    #[test]
    fn strategy_roundtrip() {
        let strategies = [
            CounterofferStrategy::StepDown,
            CounterofferStrategy::SplitDifference,
            CounterofferStrategy::AnchorHigh,
        ];
        for s in &strategies {
            let parsed = CounterofferStrategy::parse_label(s.as_str()).unwrap();
            assert_eq!(&parsed, s);
        }
        assert!(CounterofferStrategy::parse_label("bogus").is_none());
    }

    #[test]
    fn plan_respects_max_alternatives() {
        let planner = CounterofferPlanner;
        let envelope = test_envelope(vec![discount_range(10.0)]);
        let config = CounterofferConfig { max_alternatives: 5, ..Default::default() };

        let plan = planner.plan(&envelope, &config);

        assert!(plan.alternatives.len() <= 5);
    }
}
