//! Deterministic counteroffer planner for NXT negotiation autopilot.
//!
//! Given a concession envelope, generates ranked counteroffer alternatives
//! with stable ordering and deterministic tie-break behavior.
//!
//! Strategy templates define how alternatives are generated (e.g., step-down
//! from ceiling, split-the-difference, anchor-high). The planner applies the
//! selected strategy and ranks results by a configurable priority field.

use serde::{Deserialize, Serialize};

use crate::domain::negotiation::{
    ConcessionEnvelope, ConcessionRange, CounterofferAlternative, CounterofferPlan,
};

// ---------------------------------------------------------------------------
// Strategy templates
// ---------------------------------------------------------------------------

/// Strategy for generating counteroffer alternatives.
#[derive(Debug, Clone, PartialEq, Eq, Serialize, Deserialize)]
pub enum CounterofferStrategy {
    /// Step down from ceiling in fixed increments.
    StepDown,
    /// Split the difference between current and floor.
    SplitDifference,
    /// Anchor high: start near ceiling and offer small concessions.
    AnchorHigh,
}

impl CounterofferStrategy {
    pub fn as_str(&self) -> &'static str {
        match self {
            Self::StepDown => "step_down",
            Self::SplitDifference => "split_difference",
            Self::AnchorHigh => "anchor_high",
        }
    }

    pub fn parse_label(s: &str) -> Option<Self> {
        match s {
            "step_down" => Some(Self::StepDown),
            "split_difference" => Some(Self::SplitDifference),
            "anchor_high" => Some(Self::AnchorHigh),
            _ => None,
        }
    }
}

/// Configuration for counteroffer generation.
#[derive(Debug, Clone, PartialEq, Serialize, Deserialize)]
pub struct CounterofferConfig {
    pub strategy: CounterofferStrategy,
    /// Maximum number of alternatives to generate.
    pub max_alternatives: u32,
    /// Tie-break field for deterministic ranking.
    pub tie_break_field: String,
    /// Step size override (if None, uses dimension policy step).
    pub step_override: Option<f64>,
}

impl Default for CounterofferConfig {
    fn default() -> Self {
        Self {
            strategy: CounterofferStrategy::StepDown,
            max_alternatives: 3,
            tie_break_field: "discount_pct".to_string(),
            step_override: None,
        }
    }
}

// ---------------------------------------------------------------------------
// Planner
// ---------------------------------------------------------------------------

/// Deterministic counteroffer planner. Stateless.
#[derive(Debug, Clone, Default)]
pub struct CounterofferPlanner;

impl CounterofferPlanner {
    /// Generate a ranked counteroffer plan from the concession envelope.
    ///
    /// The primary dimension is the `tie_break_field` in config. Alternatives
    /// are generated by varying that dimension within the envelope range.
    pub fn plan(
        &self,
        envelope: &ConcessionEnvelope,
        config: &CounterofferConfig,
    ) -> CounterofferPlan {
        let primary_range = envelope.ranges.iter().find(|r| r.dimension == config.tie_break_field);

        let alternatives = match primary_range {
            Some(range) => match config.strategy {
                CounterofferStrategy::StepDown => self.step_down(range, config),
                CounterofferStrategy::SplitDifference => self.split_difference(range, config),
                CounterofferStrategy::AnchorHigh => self.anchor_high(range, config),
            },
            None => Vec::new(),
        };

        CounterofferPlan {
            session_id: envelope.session_id.clone(),
            alternatives,
            tie_break_field: config.tie_break_field.clone(),
        }
    }

    /// Step down from ceiling in fixed increments.
    fn step_down(
        &self,
        range: &ConcessionRange,
        config: &CounterofferConfig,
    ) -> Vec<CounterofferAlternative> {
        let step = config.step_override.unwrap_or(if range.ceiling > range.floor {
            (range.ceiling - range.floor) / (config.max_alternatives as f64 + 1.0)
        } else {
            1.0
        });

        let mut alts = Vec::new();
        let mut value = range.ceiling;
        let mut rank = 1u32;

        while rank <= config.max_alternatives && value >= range.floor {
            alts.push(CounterofferAlternative {
                offer_id: format!("offer-{}-{rank}", config.strategy.as_str()),
                rank,
                discount_pct: value,
                term_months: None,
                rationale: format!(
                    "Step-down offer: {:.1}% (step {rank} of {})",
                    value, config.max_alternatives
                ),
            });
            value -= step;
            rank += 1;
        }

        alts
    }

    /// Split the difference between current and floor.
    fn split_difference(
        &self,
        range: &ConcessionRange,
        config: &CounterofferConfig,
    ) -> Vec<CounterofferAlternative> {
        let mut alts = Vec::new();
        let mut value = range.current;
        let mut rank = 1u32;

        while rank <= config.max_alternatives && value >= range.floor {
            alts.push(CounterofferAlternative {
                offer_id: format!("offer-split-{rank}"),
                rank,
                discount_pct: value,
                term_months: None,
                rationale: format!(
                    "Split-difference offer: {:.1}% (midpoint iteration {rank})",
                    value
                ),
            });
            // Each step halves the distance to floor
            value = (value + range.floor) / 2.0;
            rank += 1;
        }

        alts
    }

    /// Anchor high: start near ceiling and offer small concessions.
    fn anchor_high(
        &self,
        range: &ConcessionRange,
        config: &CounterofferConfig,
    ) -> Vec<CounterofferAlternative> {
        let total_range = range.ceiling - range.floor;
        // Start at 90% of ceiling, give small steps
        let start = range.ceiling - total_range * 0.1;
        let small_step = config.step_override.unwrap_or(total_range * 0.05);

        let mut alts = Vec::new();
        let mut value = start;
        let mut rank = 1u32;

        while rank <= config.max_alternatives && value >= range.floor {
            alts.push(CounterofferAlternative {
                offer_id: format!("offer-anchor-{rank}"),
                rank,
                discount_pct: value,
                term_months: None,
                rationale: format!("Anchor-high offer: {:.1}% (small concession {rank})", value),
            });
            value -= small_step;
            rank += 1;
        }

        alts
    }
}

